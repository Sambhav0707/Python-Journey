name = "Sambhav"

#len :- it returns the length of the string

print(len(name))

#starts with and ends with :- check that the given string is in the start or end of the string 

print(name.startswith("s"))

print(name.endswith("av"))

#capitalise :- make the first char of the word capital 

print(name.capitalize())

#find :- find the word and return its first occurrance

print(name.find(""))

#replace :- replaces the old word with the new word 

print(name.replace("Sam" , "s"))