#remove :- remove the given element

s1 = {23,3,3,3,45,6,6,88,8}

s1.remove(3)

print(s1)

#union :- add all the elements of two sets and return a new set 

s2 = {0,9,8}

s3 = s1.union(s2)

print(s3)


#intersaction :- return a set with all the common element in two sets

s3 = s1.intersection(s2)

print(s3)

